const usedRetentionIndices = new Set<number>();
const usedGiftIndices = new Set<number>();

// 随机获取一条话术（不重复）
function getRandomScript(scripts: string[], usedIndices: Set<number>): string {
    const available = [];
    for (let i = 0; i < scripts.length; i++) {
        if (!usedIndices.has(i)) {
            available.push(i);
        }
    }
    
    // 如果都用完了，重置
    if (available.length === 0) {
        usedIndices.clear();
        for (let i = 0; i < scripts.length; i++) {
            available.push(i);
        }
    }
    
    const randomIdx = available[Math.floor(Math.random() * available.length)];
    usedIndices.add(randomIdx);
    return scripts[randomIdx];
}

// 获取组合互动话术
